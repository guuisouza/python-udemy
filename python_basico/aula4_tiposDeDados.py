# Tipos int e float
# int -> Número inteiro
# int representa qualquer número positivo ou negativo
 
print(1) #int
print(-1) #int
print(0) 

# float -> Números com ponto flutuante
# float representa qualquer número positivo ou negativo com ponto flutuante
print(1.1, 10.1)
print(0.0, -1.5)

# Pra saber o tipo de valor no python usamos a função (que na verdade é classe) type
print(type("Otávio"))
print(type(15.1), type(-1.5), type(0.0))
print(type(-45), type(47))
