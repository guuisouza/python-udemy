# A função print é usada para exibir as coisas na tela
# Ela recebe um argumento entre os parenteses
# Argumento é uma coisa que passamos para as funções

# Por padrão ele coloca entre espaço na saida do terminal, mas podemos mudar o separador
# Com um argumento nomeado da função print chamado Sep, podemos mudar o separador
print(12, 34, sep=", ")
print(56, 78, sep=", ")
