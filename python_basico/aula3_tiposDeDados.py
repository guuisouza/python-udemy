"""
Python = Linguagem de programação 
Tipo de tipagem = Dinâmica/Forte
Tipagem dinâmica significa que o python já sabe
o tipo da informação que estamos passando pra ele
str -> String -> Texto 
Strings são textos que estão dentro de aspas
"""

#O que é tipagem Dinâmica? Por exemplo ao passar um print
print(1234) #Ele já sabe o tipo de dado ele já detecta que é um número

#Aspas Simples
print('Luis Otavio') #Ele já sabe que é uma string apenas por estar entre aspas
print(1, 'Luis "Otavio"') 

#Aspas Duplas
print("Luis Otavio") #Ele já sabe que é uma string apenas por estar entre aspas

#Escape
print("Luis \"Otavio\"") 

#Para fazer o caractere de escape aparecer usamos o r
