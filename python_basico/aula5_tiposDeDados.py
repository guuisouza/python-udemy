# # Tipo de dado boolean
# Ao questionar algo em um programa
# Só existe duas respostas possíveis
# sim(True) e não(False)
# Existem vários operadores para questionar
# Dentre eles o ==, que questiona se um valor é igual a outro
print(10 == 10) # Sim -> True (Verdadeiro)
print(10 == 11) # Não -> False (Falso)
print(type(10 == 10)) # Vai retornar o tipo boolean e não o valor em si
print(type(10 == 1))