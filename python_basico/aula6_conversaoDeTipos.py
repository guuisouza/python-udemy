# conversão de tipos, coerção
# type convertion, typecasting, coercion
# é o ato de converter um tipo em outro
# tipos imutáveis e primitivos:
# str, float, int e bool 
print(int('1'), type(int('1'))) # usamos a classe int para converter a string em um número
print(float('1') + 1)
print(bool(' ')) # uma string vazia é considerada false e uma com algum valor (espaço também) é true
print(str(11) + 'b')